def compare_lists(llist1, llist2):
    # Traverse both lists simultaneously
    current1 = llist1
    current2 = llist2
    
    while current1 is not None and current2 is not None:
        # If the data values are not equal, the lists are not equal
        if current1.data != current2.data:
            return 0
        
        # Move to the next nodes
        current1 = current1.next
        current2 = current2.next
    
    # After the loop, check if both lists ended at the same time
    # This means they had the same length and all data was equal
    if current1 is None and current2 is None:
        return 1
    else:
        # If one list is longer than the other, they are not equal
        return 0

