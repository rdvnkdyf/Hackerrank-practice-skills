def getNode(llist, positionFromTail):
    # Initialize two pointers, 'fast' and 'slow', at the head of the list.
    fast = llist
    slow = llist

    # Move the 'fast' pointer forward by 'positionFromTail' steps.
    # This creates the desired gap between the two pointers.
    for _ in range(positionFromTail):
        fast = fast.next

    # Move both pointers forward one step at a time until 'fast' reaches the end of the list.
    # When 'fast' is at the last node, 'slow' will be at the desired position.
    while fast.next is not None:
        fast = fast.next
        slow = slow.next

    # Return the data value of the node pointed to by 'slow'.
    return slow.data

